print("konversi suhu (farenheit ke celcius)")

#input suhu dalam farenheit
F = float(input("masukkan suhu(farenheit): "))

#melakukan konversi suhu ke celcius
C = 5 * (F-32) / 9

#menampilkan hasil konversi ke layar
print("farenheit: ", F)
print("celcius: ", C)